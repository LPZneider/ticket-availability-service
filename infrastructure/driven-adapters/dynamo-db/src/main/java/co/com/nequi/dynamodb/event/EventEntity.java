package co.com.nequi.dynamodb.event;

import software.amazon.awssdk.enhanced.dynamodb.mapper.annotations.DynamoDbBean;
import software.amazon.awssdk.enhanced.dynamodb.mapper.annotations.DynamoDbPartitionKey;
import software.amazon.awssdk.enhanced.dynamodb.mapper.annotations.DynamoDbSortKey;

/**
 * Single-table item read from TICKETS_TABLE_NAME: pk=eventId, sk="METADATA".
 * Written by ticket-reservation-service; this service only reads it. Ticket
 * items share the same eventId partition (pk=eventId, sk=ticketId), which is
 * what makes a single Query by pk return the event plus all its tickets.
 */
@DynamoDbBean
public class EventEntity {

    public static final String METADATA_SORT_KEY = "METADATA";

    private String pk;
    private String sk;
    private String eventId;
    private String name;
    private String date;
    private String venue;
    private int totalCapacity;
    private int availableCount;

    public EventEntity() {
    }

    @DynamoDbPartitionKey
    public String getPk() {
        return pk;
    }

    public void setPk(String pk) {
        this.pk = pk;
    }

    @DynamoDbSortKey
    public String getSk() {
        return sk;
    }

    public void setSk(String sk) {
        this.sk = sk;
    }

    public String getEventId() {
        return eventId;
    }

    public void setEventId(String eventId) {
        this.eventId = eventId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getVenue() {
        return venue;
    }

    public void setVenue(String venue) {
        this.venue = venue;
    }

    public int getTotalCapacity() {
        return totalCapacity;
    }

    public void setTotalCapacity(int totalCapacity) {
        this.totalCapacity = totalCapacity;
    }

    public int getAvailableCount() {
        return availableCount;
    }

    public void setAvailableCount(int availableCount) {
        this.availableCount = availableCount;
    }
}
